"""Tests for gcode_compat — generic slicer G-code → BBL firmware translation."""

from bambox.gcode_compat import (
    _build_header_block,
    _parse_prusa_time,
    is_bbl_gcode,
    translate_to_bbl,
)


# --- is_bbl_gcode ---


def test_orca_gcode_detected():
    gcode = b"; HEADER_BLOCK_START\n; total layer number: 50\n; HEADER_BLOCK_END\n"
    assert is_bbl_gcode(gcode) is True


def test_cura_gcode_not_bbl():
    gcode = b";FLAVOR:Marlin\n;LAYER_COUNT:50\n;LAYER:0\n"
    assert is_bbl_gcode(gcode) is False


def test_prusa_gcode_not_bbl():
    gcode = b"; generated by PrusaSlicer\n;LAYER_CHANGE\n;HEIGHT:0.2\n"
    assert is_bbl_gcode(gcode) is False


def test_accepts_str():
    assert is_bbl_gcode("; HEADER_BLOCK_START\n; total layer number: 1\n") is True


# --- translate_to_bbl (passthrough) ---


def test_bbl_gcode_unchanged():
    gcode = b"; HEADER_BLOCK_START\n; total layer number: 50\n; HEADER_BLOCK_END\nG28\n"
    assert translate_to_bbl(gcode) == gcode


# --- translate_to_bbl (CuraEngine) ---


CURA_GCODE = (
    b";FLAVOR:Marlin\n"
    b";TIME:338\n"
    b";Filament used: 0.33m\n"
    b";Layer height: 0.2\n"
    b";MAXZ:10\n"
    b";TARGET_MACHINE.NAME:BambuLab P1S\n"
    b"\n"
    b";LAYER_COUNT:3\n"
    b";LAYER:0\n"
    b"G1 Z0.2 F3000\n"
    b"G1 X10 Y10 E1\n"
    b";LAYER:1\n"
    b"G1 Z0.4\n"
    b";LAYER:2\n"
    b"G1 Z0.6\n"
)


def test_cura_header_block():
    result = translate_to_bbl(CURA_GCODE).decode()
    assert result.startswith("; HEADER_BLOCK_START\n")
    assert "; HEADER_BLOCK_END\n" in result
    assert "; total layer number: 3" in result
    assert "; max_z_height: 10" in result
    assert "; total estimated time: 5m 38s" in result


def test_cura_layer_markers():
    result = translate_to_bbl(CURA_GCODE).decode()

    # Layer 0 → 1/3
    assert "; layer num/total_layer_count: 1/3" in result
    assert "M73 L1\n" in result
    assert "M991 S0 P0 ;notify layer change" in result

    # Layer 2 → 3/3
    assert "; layer num/total_layer_count: 3/3" in result
    assert "M73 L3\n" in result
    assert "M991 S0 P2 ;notify layer change" in result


def test_cura_progress_commands():
    """M73 P{pct} R{remaining_min} injected at each layer."""
    result = translate_to_bbl(CURA_GCODE).decode()
    # Layer 0: 0% done, full time remaining (338s ≈ 6min)
    assert "M73 P0 R6\n" in result
    # Layer 1: 33% done
    assert "M73 P33 R4\n" in result
    # Layer 2: 67% done
    assert "M73 P67 R2\n" in result


def test_cura_original_markers_preserved():
    result = translate_to_bbl(CURA_GCODE).decode()
    assert ";LAYER_COUNT:3" in result
    assert ";LAYER:0" in result
    assert ";LAYER:2" in result


def test_cura_movement_preserved():
    result = translate_to_bbl(CURA_GCODE).decode()
    assert "G1 Z0.2 F3000" in result
    assert "G1 X10 Y10 E1" in result


